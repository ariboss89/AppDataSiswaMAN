    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String nip = txtNip.getText().trim();
        String nama = txtNama.getText().trim();
        String jenisKelamin = cbJenisKelamin.getSelectedItem().toString().trim();
        String agama = "ISLAM";
        String status = cbStatus.getSelectedItem().toString();
        String bidang = cbBidang.getSelectedItem().toString();

        if (nip.equals("")) {
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong !!");
            txtNip.requestFocus();
        } else if (nama.equals("")) {
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong !!");
            txtNama.requestFocus();
        } else if (status.equals("PILIH") || (bidang.equals("PILIH"))) {
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Dahulu !!");

        } else {
            gru.Save(nip, nama, jenisKelamin, agama, status, bidang);
            Refresh();
        }

    }//GEN-LAST:event_btnSaveActionPerformed

    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        // TODO add your handling code here:
        if (evt.getKeyChar() == KeyEvent.VK_DELETE) {
            int ok = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Menghapus ?", "Konnfirmasi", JOptionPane.YES_NO_OPTION);
            if (ok == 0) {
                gru.Delete(txtNip.getText().trim());
                rss = gru.Show();
                tbl.SetTabel(jTable1, rss, namaKolom, jmlKolom, lebar);
                Refresh();
            } else {

            }
        }
    }//GEN-LAST:event_jTable1KeyPressed

