    void ShowTA(){
        con = new Koneksi();
        try{
            st = con.connect().createStatement();
            rs = st.executeQuery("SELECT *FROM tb_ta");
            while(rs.next()){
                lblTA.setText(rs.getString("tahun_ajaran"));
            }
        }catch(SQLException ex){
            
        }
    }
    
