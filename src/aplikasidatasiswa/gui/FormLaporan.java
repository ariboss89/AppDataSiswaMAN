    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        con = new Koneksi();
            try {
                HashMap parameter = new HashMap();
                File file = new File("src/aplikasidatasiswa/report/LaporanDataSiswa.jasper");
                String TA = lblTA.getText();
                parameter.put("ta", TA);
                String kelas = cbKelas.getSelectedItem().toString();
                parameter.put("kelas", kelas);
                String jurusan = cbJurusan.getSelectedItem().toString();
                parameter.put("jurusan", jurusan);
                
                JasperReport jp = (JasperReport) JRLoader.loadObject(file);
                JasperPrint jasperPrint = JasperFillManager.fillReport(jp, parameter, con.connect());
                JasperViewer.viewReport(jasperPrint, false);
                JasperViewer.setDefaultLookAndFeelDecorated(true);
                Refresh();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        java.sql.Connection conn= new Koneksi().connect();
            try{
                File file = new File("src/aplikasidatasiswa/report/LaporanDataGuru.jrxml");
                JasperDesign jasperDesign = JRXmlLoader.load(file);
                String sql="select * from tb_guru";
                JRDesignQuery newQuery=new JRDesignQuery();
                newQuery.setText(sql);
                jasperDesign.setQuery(newQuery);
                JasperReport jasperReport=JasperCompileManager.compileReport(jasperDesign);
                JasperPrint jasperPrint=JasperFillManager.fillReport(jasperReport, null, conn);
                JasperViewer.viewReport(jasperPrint,false);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
    }//GEN-LAST:event_jButton2ActionPerformed

